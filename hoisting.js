/*

    O CONCEITO DE HOISTING É UM CONCEITO PADRÃO DE MOVER A DECLARAÇÃO DE VARIÁVEL PARA O TOP OU PARA CIMA
    O conceito de hoisting consiste em içar ou mandar para cima uma variável, mesmo que não tenha sido declarada
    e colocá-la como undefined
    o que ela faz é semelhante a isso:
    var a;
    console.log("valor de a = ", a);
    var a = 2;

    ISSO ACONTECERIA SE EU USAR NUMA FUNÇÃO

O EFEITO DE HOISTING OU IÇAMENTO SE EU DECLARAR A VARIÁAVEL COM LET NÃO OCORRE
*/

